package _02_operators._09_conditional;

public class Demo1 {
    public static void main(String[] args) {
        System.out.println(true ? "its true" : "its false");//its true
        System.out.println(false ? "its true" : "its false");//its false
        System.out.println(10 % 2 == 0 ? "its even" : "its odd");//its even
        System.out.println(11 % 2 == 0 ? "its even" : "its odd");//its odd
    }
}

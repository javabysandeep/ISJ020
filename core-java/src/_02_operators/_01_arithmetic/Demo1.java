package _02_operators._01_arithmetic;

public class Demo1 {
    public static void main(String[] args) {
        System.out.println("Mathematical Addition = +");
        System.out.println(10 + 20); //30

        System.out.println("Mathematical Subtraction = -");
        System.out.println(20 - 10); //10

        System.out.println("Mathematical Multiplication = *");
        System.out.println(20 * 10);//200

        System.out.println("Mathematical Division Quotient = /");
        System.out.println(11 / 2); //Quotient = 5
        System.out.println(11.0 / 2); //Quotient = 5.5

        System.out.println("Mathematical Division Remainder = %");
        System.out.println(11 % 2); //Remainder = 1
    }
}

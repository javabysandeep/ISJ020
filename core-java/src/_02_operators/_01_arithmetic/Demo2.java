package _02_operators._01_arithmetic;

public class Demo2 {
    public static void main(String[] args) {
        System.out.println(11.0 % 2);//1.0
        System.out.println(10 / 20);//0
        System.out.println(10.0 / 20);//0.5
        System.out.println(99 / 100);//0
        System.out.println(99.0 / 100);//0.99
        System.out.println('A' + 30);//65+30=95

        System.out.println(1 / 10);//0
        System.out.println(10 / 1);//10
        System.out.println(10 / 2);//5

        System.out.println(0 / 1);//0
        System.out.println(0 % 1);//0
    }
}

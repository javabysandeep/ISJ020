package _02_operators._06_relationalOperators;

public class Demo1 {
    public static void main(String[] args) {
        System.out.println(10 > 10); //false
        System.out.println(10 >= 10); //true
        System.out.println(10 < 10); //false
        System.out.println(10 <= 10);//true
        System.out.println(10 == 10);//true
        System.out.println(10 != 10);//false
    }
}

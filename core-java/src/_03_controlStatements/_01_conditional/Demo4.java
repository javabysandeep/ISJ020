package _01_introduction._03_controlStatements._01_conditional;

public class Demo4 {
    public static void main(String[] args) {
        int number = 10;

        if (number % 2 == 0) {
            System.out.println("Its an even number");
        }

        if (number % 2 != 0) {
            System.out.println("Its an odd number");
        }
    }
}
